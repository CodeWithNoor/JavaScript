// boolean & comparision operator

// boolean 
// true & false

// ===  --->  equal value and equal type
// ==  --->  equal to

let a = "9"; // check value not data type only JS
let b = 9;

console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);

// === vs ==

console.log(a == b);
console.log(a === b); // check value with data type

// !== vs !=
console.log(a != b);
console.log(a !== b); 
