// Rules for naming variable

// You can not start from number
// Example: 
// 1value --> invalid
// value1 --> valid

var value1 = 2;
console.log(value1 ** 2); // Power of number 2

// You can use only underscore or dollar
// first_name --> valid
// _firstname --> valid

var first_name = "Harshit";
console.log(first_name);

console.log(first_name,"Kartik");

// fisrt$name --> valid
// $firstname --> valid

var fisrt$name = "Naira";
console.log(fisrt$name);

// You can not use spaces
// first name --> invalid

// convention
// Start with small letter and use camelCase